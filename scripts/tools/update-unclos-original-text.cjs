// UNCLOSディレクトリのパス
const unclosDir = path.join(__dirname, '../../src/data/laws/treaty/unclos');

// メイン処理
function main() {
  console.log('='.repeat(60));
  console.log('国連海洋法条約 YAMLファイル 原文更新スクリプト');
  console.log('='.repeat(60));

  let updated = 0;
  let skipped = 0;
  let errors = 0;

  for (const [articleNum, originalText] of Object.entries(UNCLOS_ORIGINAL_TEXTS)) {
    const filename = `${articleNum}.yaml`;
    const filepath = path.join(unclosDir, filename);

    try {
      // ファイルが存在するか確認
      if (!fs.existsSync(filepath)) {
        console.log(`  ${filename} が存在しません`);
        skipped++;
        continue;
      }

      // YAMLファイルを読み込み
      const content = fs.readFileSync(filepath, 'utf8');
      const data = yaml.load(content);

      // 原文を更新
      const oldText = data.originalText;
      data.originalText = originalText;

      // YAMLファイルを書き込み
      const newContent = yaml.dump(data, {
        lineWidth: -1,
        noRefs: true,
        quotingType: "'",
        forceQuotes: false,
      });

      fs.writeFileSync(filepath, newContent, 'utf8');

      console.log(`[OK] ${filename}: 原文を更新しました（${originalText.length}項目）`);
      updated++;
    } catch (error) {
      console.error(`[NG] ${filename}: エラー - ${error.message}`);
      errors++;
    }
  }

  console.log('\n' + '='.repeat(60));
  console.log(`完了: 更新 ${updated}件, スキップ ${skipped}件, エラー ${errors}件`);
  console.log('='.repeat(60));
  console.log('\n注意: 残りの条文は公式文書から原文を取得する必要があります。');
}

main();
